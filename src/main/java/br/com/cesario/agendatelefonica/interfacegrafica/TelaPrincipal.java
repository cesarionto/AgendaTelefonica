    public void listar() {
        DefaultTableModel modelo;
        modelo = (DefaultTableModel) jTableContatos.getModel();
        modelo.setNumRows(0);

        ContatosDAO dao = new ContatosDAO();
        for (Contatos contatos : dao.listar()) {
            modelo.addRow(new Object[]{
                contatos.getId(),
                contatos.getNome(),
                contatos.getNumero_telefone()
            });
        }
    }

